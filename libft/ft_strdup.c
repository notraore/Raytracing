#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s)
{
	char			*dup;
	unsigned long	len;
	unsigned long	i;

	i = 0;
	len = ft_strlen(s);
	if (!(dup = (char*)malloc(sizeof(const char) * len + 1)))
		return (NULL);
	if (dup == NULL)
		return (NULL);
	while (i < len)
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}

#include "libft.h"

int	ft_strequ(const char *s1, const char *s2)
{
	unsigned long	i;

	i = 0;
	while (s1[i] == s2[i] && (s1[i] != '\0' || s2[i] != '\0'))
		i++;
	if (s1[i] == s2[i])
		return (1);
	else
		return (0);
}
